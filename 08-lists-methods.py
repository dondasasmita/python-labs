# sort method in an int list will return the lowest number to the highest
numbers = [15, 30, 31, 1, 7, 84, 81]
numbers.sort()
print(numbers)

# join method will return the values of the list joined by the values that was passed in
idiom = ["better", "late", "than", "never"]
print(idiom)
print()
print("\n".join(idiom))
print()
print("-".join(idiom))
